    private void btnRecargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecargarActionPerformed
        //Variables
        boolean consultaSaldo = false, consultaSaldoInicial = false;
        int numReg = 0;
        int idOperador;
        String usernameP, celular, montoR;
        double saldoP = 0, montoDoubleR = 0, monto = 0, saldoInicial = 0, restarSaldoPrincipal = 0;

        //Instancia usuario logeado principal
        var up = new Usuario();

        //User logeado realiza recarga $
        usernameP = txtUsernameP.getText();

        //Celular a realizarle recarga $
        celular = txtCelularRC.getText();

        //Operador escogido en el select para la recarga $
        idOperador = 0;

        //Variable montoR para guardar
        montoR = txtCuantoRC.getText();

        //Try para extraer saldo inicial del usuario P
        try {
            consultaSaldoInicial = up.consultarSaldo(usernameP);
            if (consultaSaldoInicial) {
                //Guardo el saldo inicial del usuario P
                saldoInicial = up.getSaldo();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

        //Reset el select operador
        if ((cmbOperadorRC.getSelectedIndex() != -1)) {
            idOperador = cmbOperadorRC.getItemAt(cmbOperadorRC.getSelectedIndex()).getId();
        }
        //Valido que el celular este vacio
        if (txtCelularRC.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo 'celular' esta vacio.");
        } //Valido que el operador haya sido escogido
        else if (cmbOperadorRC.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Escoge un operador");

            //Valido que monto sea mayor o este vacio
        } else if (txtCuantoRC.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo 'cuanto' esta vacio, ingresa datos. \n"
                + "y el valor a recargar debe ser de 1000 COP en adelante.");

            //Valido que el monto sea menor a saldoInicial
        } else if (monto > saldoInicial) {
            JOptionPane.showMessageDialog(null, "No tiene saldo suficiente.");
        } else {
            try {
                consultaSaldo = up.consultarSaldo(usernameP);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
            //Primer if
            if (consultaSaldo) {
                //Saldo del usuario principal que envia o recarga
                saldoP = up.getSaldo();
                //parseo de saldo y proceso resta
                try {
                    montoDoubleR = Double.parseDouble(montoR);
                    if (montoDoubleR < 1000) {
                        JOptionPane.showMessageDialog(null, "El valor a recargar debe ser de 1000 COP en adelante.");
                    } else {
                        restarSaldoPrincipal = saldoP - montoDoubleR;
                        numReg = up.modificarsaldo(usernameP, restarSaldoPrincipal);
                        numReg = up.insertarHistorial(usernameP, celular, idOperador, montoDoubleR);
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
                if (numReg > 0) {
                    JOptionPane.showMessageDialog(null, "Recarga Exitosa");
                } else {
                    JOptionPane.showMessageDialog(null, "Error. No se pudo realizar la recarga.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Error2.");
            }
        }
    }//GEN-LAST:event_btnRecargarActionPerformed

