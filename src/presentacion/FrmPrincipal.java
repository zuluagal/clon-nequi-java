    private void btnEnviarDineroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarDineroActionPerformed
        //Variables
        boolean consultaSaldo = false;
        int numReg = 0;
        String usernameP, usernameR, montoR, mensaje = "";
        double montoDoubleR = 0, saldoInicial = 0, saldoInicialP = 0, saldoInicialR = 0, restarSaldoPrincipal = 0, sumarSaldoRecibe = 0;

        //Instancia usuario logeado principal
        var up = new Usuario();
        //Instancia usuario que recibe dinero enviado
        var ur = new Usuario();

        //User logeado envia $
        usernameP = txtUsernameP.getText();
        //User que recibe $
        usernameR = txtUsernameR.getText();
        //Variable montor para guardar
        montoR = txtMontoR.getText();
        //Var mensaje
        mensaje = txtAreaMensajeR.getText();

        if (txtUsernameR.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo usuario esta vacio, ingresa datos.");
        } else if (txtMontoR.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo monto esta vacio, ingresa datos. \n"
                + "y el valor a enviar debe ser de 5000 COP en adelante.");
        } else if (usernameP.equals(usernameR)) {
            JOptionPane.showMessageDialog(null, "No puedes enviar dinero a ti mismo.");
        } else {
            //Try para consultar el saldo de ambos usuarios
            try {
                //Saldo del usuario Princial que envia
                consultaSaldo = up.consultarSaldo(usernameP);
                //Saldo del que recibe
                consultaSaldo = ur.consultarSaldo(usernameR);
                //Primer IF
                if (consultaSaldo) {
                    //Saldo usu principal logeado
                    saldoInicialP = up.getSaldo();
                    //Saldo usu que recibe dinero
                    saldoInicialR = ur.getSaldo();

                    //parseo de saldo y proceso resta
                    try {
                        montoDoubleR = Double.parseDouble(montoR);
                        if (montoDoubleR < 5000) {
                            JOptionPane.showMessageDialog(null, "El valor a enviar debe ser de 5000 COP en adelante.");
                        } else {
                            restarSaldoPrincipal = saldoInicialP - montoDoubleR;
                            numReg = up.modificarsaldo(usernameP, restarSaldoPrincipal);
                            sumarSaldoRecibe = saldoInicialR + montoDoubleR;
                            numReg = ur.modificarsaldo(usernameR, sumarSaldoRecibe);
                            
                        }
                    } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(null, ex);
                    }
                    //numReg > 0 o no
                    if (numReg > 0) {
                        JOptionPane.showMessageDialog(null, "Transferencia Exitosa");
                    } else {
                        JOptionPane.showMessageDialog(null, "Error, no se realizo la transferencia.");
                    }
                    //if consultaSaldo usuario no existe
                } else {
                    JOptionPane.showMessageDialog(null, "El usuario '" + usernameR + "' no existe.");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_btnEnviarDineroActionPerformed

